### entend() - Add Elements of List 1 to End of List 2 ###

# Initialize Lists
List1 = ["carrot", "potato", "lettuce", "beans", "corn"]
List2 = ["a","b","c","d","e"]

# List1.extend(List2) -- Add elements from List2 to end of List1
List1.extend(List2)

# Print updated list
print (List1) # Output: ['carrot', 'potato', 'lettuce', 'beans', 'corn', 'a', 'b', 'c', 'd', 'e']
