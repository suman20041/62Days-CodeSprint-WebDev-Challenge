### range() - Print list of numbers in a range ###

# list(range(start, end, step) -- Define Starting Value, Ending Value, Step Value to increase by
print(list(range(1, 500, 2))) # Print list of numbers in range
  # 1 = Starting value in range
  # 500 = Ending value in range
  # 2 = Step Value to increase by (or any value you want)
