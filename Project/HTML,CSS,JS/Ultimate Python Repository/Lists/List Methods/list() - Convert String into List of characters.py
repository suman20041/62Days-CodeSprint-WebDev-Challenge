### list() - Convert String Into List of characters ###


# Initialize a String
word = "Word1 Word2"

# list(String) -- Convert String into List
print (list(word)) # Print the string converted into a list
                   # Output: ['W', 'o', 'r', 'd', '1', ' ', 'W', 'o', 'r', 'd', '2']

