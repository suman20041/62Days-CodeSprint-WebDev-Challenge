### max(), min() - Find Maximum and Minimum values in list ###


# Initialize List
list1 = [12,43,21,1]

# max(List) -- Find max/largest value in the list
max(list1)

print("Max value in the list: ", max(list1)) # Output: 43

## -----------------------------------------------------------------------------------------

# min(List) -- Find min/smallest value in the list
min(list1)

print("Min value in the list: ", min(list1)) # Output: 1
