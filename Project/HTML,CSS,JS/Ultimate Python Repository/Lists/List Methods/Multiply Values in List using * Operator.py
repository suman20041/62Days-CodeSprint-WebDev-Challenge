### Multiply Values in List using * Operator ###

# Initialize Lists
a = [10, 20, 30, 40, 50]
b = [60, 70, 80, 90, 100]

# List * int : Multiply list by an integer value
print (a*3) # Output: [10, 20, 30, 40, 50, 10, 20, 30, 40, 50, 10, 20, 30, 40, 50]

# Multiply list by an int
print (b*5) # Output: [60, 70, 80, 90, 100, 60, 70, 80, 90, 100, 60, 70, 80, 90, 100, 60, 70, 80, 90, 100, 60, 70, 80, 90, 100]
