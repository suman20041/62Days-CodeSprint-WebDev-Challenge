### if-else-elif Statement ###

## Print Lowest of 2 Numbers

n1 = int(input("Enter a number: "))
n2 = int(input("Enter another number: "))

# if (condition)
if (n1<n2):
    print (n1, "<", n2)
# elif (condition)
elif (n1==n2):
    print (n1, "=", n2)
# else (condition)
else:
    print (n2, "<", n1)

